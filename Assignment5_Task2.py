#creating list of 1 to 10
l = list(range(1,11))

#slicing first five elements of list
ll = l[:5]

#revers slicing the string to reverse it.
llr = ll[::-1]

#printing output
print("Original list: {})".format(l))
print("Extracted list: {})".format(ll))
print("Reverse extracted list: {})".format(llr))
